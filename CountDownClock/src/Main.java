    // Count down function
    public void startCountdown() {
        timer = new Timer(1000, new ActionListener() { // Setting an delay of 1000ms or 1 sec for updation
            @Override
            public void actionPerformed(ActionEvent e) {
                if (seconds == 0 && minutes == 0 && hours == 0) { // checking if the timer is finished
                    timer.stop();
                    jLabel6.setText("Time's up!"); 
                    jComboBox1.setEnabled(true);
                    jComboBox2.setEnabled(true);
                    jComboBox3.setEnabled(true);
                    jButton1.setText("Start"); 
                    playRingtone(); 
                    jButton4.setText("Listen Ringtone");

                    String[] options = {"Stop"};  // Setting option pane for stoping the clock
                    javax.swing.JOptionPane.showOptionDialog( 
                            SwingUtilities.getWindowAncestor(jButton1),
                            "Timer Finished!",
                            "Timer Finished",
                            javax.swing.JOptionPane.DEFAULT_OPTION,
                            javax.swing.JOptionPane.INFORMATION_MESSAGE,
                            null,
                            options,
                            options[0]
                    );
                    stopRingtone();
                    jLabel6.setText("");
                    return;
                }

                // Time decrement logic
                if (seconds == 0) { 
                    if (minutes == 0) {
                        if (hours != 0) { 
                            hours--;
                            minutes = 59; // after an hour is passed we are decrementing it and setting minutes
                        }
                    } else { 
                        minutes--;
                        seconds = 59; 
                    }
                } else {
                    seconds--; // decrementing seconds
                }

                jComboBox1.setSelectedIndex(hours); 
                jComboBox2.setSelectedIndex(minutes);
                jComboBox3.setSelectedIndex(seconds);
            }
        });
        timer.start();
    }
    
    // ringtone function
    private void playRingtone() {
        if (selectedFile != null && selectedFile.exists() && selectedFile.isFile()) { // Checking for file
            try {
                ringtoneFile = new FileInputStream(selectedFile);
                player = new Player(ringtoneFile);
                playerThread = new Thread(() -> { // Creating thread for player
                    try {
                        player.play();
                        player = null;
                        playerThread = null;
                    } catch (JavaLayerException e) {
                        e.printStackTrace();
                        SwingUtilities.invokeLater(() -> jLabel6.setText("Error playing MP3: " + e.getMessage()));
                    }
                });
                playerThread.start(); // Starting player
                jButton4.setText("Stop Ringtone");
            } catch (FileNotFoundException | JavaLayerException e) {
                e.printStackTrace();
                SwingUtilities.invokeLater(() -> jLabel6.setText("Error initializing MP3 player: " + e.getMessage()));
            }
        } else {
            JOptionPane.showMessageDialog(
               this, 
               "No valid ringtone file selected.",
               "Timer Status", 
               JOptionPane.ERROR_MESSAGE
            );
            jButton4.setText("Listen Ringtone");
        }
    }

    // Function for stopping ringtone
    private void stopRingtone() {
        if (player != null && playerThread != null) { 
            player.close();
            playerThread.interrupt();
            player = null;
            playerThread = null;
        }
    }
    
    // Function for Start button
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (selectedFile == null) { // Checking for file
            javax.swing.JOptionPane.showMessageDialog(
                null,
                "Please select a song file before starting the timer.", 
                "No Song File Selected", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if ((jButton1.getText()).equals("Start")) { // Starting count down
            hours = Integer.parseInt((String) jComboBox1.getSelectedItem()); // Taking inputs
            minutes = Integer.parseInt((String) jComboBox2.getSelectedItem());
            seconds = Integer.parseInt((String) jComboBox3.getSelectedItem());

            jComboBox1.setEnabled(false); // Disabling input boxes
            jComboBox2.setEnabled(false);
            jComboBox3.setEnabled(false);

            jButton1.setText("Pause");
            startCountdown();  // Starting countdown
            JOptionPane.showMessageDialog( 
                    this, 
                    "Countdown Started", 
                    "Timer Status", 
                    JOptionPane.INFORMATION_MESSAGE,
                    clock
            );
        } else if ((jButton1.getText()).equals("Pause")) { // To pause the countdown
            timer.stop(); // Stopping countdown
            jComboBox1.setEnabled(true);
            jComboBox2.setEnabled(true);
            jComboBox3.setEnabled(true);
            jButton1.setText("Resume");
            JOptionPane.showMessageDialog( 
                this,
                "Countdown Paused", 
                "Timer Status", 
                JOptionPane.INFORMATION_MESSAGE,
                clock
            );
        } else if ((jButton1.getText()).equals("Resume")) { // Restarting Countdown
                hours = Integer.parseInt((String) jComboBox1.getSelectedItem());
                minutes = Integer.parseInt((String) jComboBox2.getSelectedItem());
                seconds = Integer.parseInt((String) jComboBox3.getSelectedItem());
                jComboBox1.setEnabled(false);
                jComboBox2.setEnabled(false);
                jComboBox3.setEnabled(false);
                jButton1.setText("Pause");
                startCountdown();
                JOptionPane.showMessageDialog( 
                    this, 
                    "Countdown Resumed", 
                    "Timer Status", 
                    JOptionPane.INFORMATION_MESSAGE,
                    clock
                );
        }
    }//GEN-LAST:event_jButton1ActionPerformed

