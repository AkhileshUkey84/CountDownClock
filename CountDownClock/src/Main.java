    // Function of select file button
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser(); // To take file input
        FileNameExtensionFilter filter = new FileNameExtensionFilter("MP3 Files", "mp3"); // filter for mp3 files only
        fileChooser.setFileFilter(filter);
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) { // Checking for selected file
            selectedFile = fileChooser.getSelectedFile();
            if (selectedFile.exists() && selectedFile.isFile()) { // checking if file exists 
                try {
                    ringtoneFile = new FileInputStream(selectedFile);
                    JOptionPane.showMessageDialog(
                    this, 
                    "Ringtone selected: " + selectedFile.getName(), 
                    "Timer Status", 
                    JOptionPane.INFORMATION_MESSAGE,
                    music
                    );
                } catch (FileNotFoundException e) { // Error if file is not selected as expected
                    JOptionPane.showMessageDialog(
                        this, 
                        "Error selecting file.", 
                        "Error", 
                        JOptionPane.ERROR_MESSAGE
                    );

                    e.printStackTrace();
                }
            } else { // if file does not exists
                JOptionPane.showMessageDialog( 
                    this,
                    "Selected file does not exist or is invalid.", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE
                );
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

